#include "./../inc/libft.h"

void		ft_free(char **array)
{
	size_t i;

	i = 0;
	if (array)
	{
		while (array[i])
		{
			free(array[i]);
			array[i] = NULL;
			i++;
		}
		free(array);
	}
}
