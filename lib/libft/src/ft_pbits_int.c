#include "./../inc/libft.h"

void		ft_pbits_int(int n)
{
	size_t	bits;
	int		i;
	char	c;

	bits = sizeof(n) * 8;
	i = bits - 1;
	while (i >= 0)
	{
		c = (n & (1 << i)) ? '1' : '0';
		ft_putchar(c);
		if (i % 4 == 0 && i)
			ft_putchar(' ');
		--i;
	}
}
